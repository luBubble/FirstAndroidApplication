package com.task.Bean;

public class School {
	private int School_id;
	private int School_num;
	private String School_name;
	private String School_account;
	private String School_password;
	public int getSchool_id() {
		return School_id;
	}
	public void setSchool_id(int school_id) {
		School_id = school_id;
	}
	public int getSchool_num() {
		return School_num;
	}
	public void setSchool_num(int school_num) {
		School_num = school_num;
	}
	public String getSchool_name() {
		return School_name;
	}
	public void setSchool_name(String school_name) {
		School_name = school_name;
	}
	public String getSchool_account() {
		return School_account;
	}
	public void setSchool_account(String school_account) {
		School_account = school_account;
	}
	public String getSchool_password() {
		return School_password;
	}
	public void setSchool_password(String school_password) {
		School_password = school_password;
	}
}
