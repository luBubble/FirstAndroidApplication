package com.task.Bean;

public class Teacher {
	private int Teacher_id;
	private int Teacher_schoolId;
	private String Teacher_tel;
	private String Teacher_password;
	private String Teacher_name;
	private String Teacher_account;
	private int Teacher_num;
	private String Teacher_description;

	public int getTeacher_schoolId() {
		return Teacher_schoolId;
	}
	public void setTeacher_schoolId(int teacher_schoolId) {
		Teacher_schoolId = teacher_schoolId;
	}
	public String getTeacher_account() {
		return Teacher_account;
	}
	public void setTeacher_account(String teacher_account) {
		Teacher_account = teacher_account;
	}
	public int getTeacher_num() {
		return Teacher_num;
	}
	public void setTeacher_num(int teacher_num) {
		Teacher_num = teacher_num;
	}
	public String getTeacher_description() {
		return Teacher_description;
	}
	public void setTeacher_description(String teacher_description) {
		Teacher_description = teacher_description;
	}
	public int getTeacher_id() {
		return Teacher_id;
	}
	public void setTeacher_id(int teacher_id) {
		Teacher_id = teacher_id;
	}
	
	public String getTeacher_tel() {
		return Teacher_tel;
	}
	public void setTeacher_tel(String teacher_tel) {
		Teacher_tel = teacher_tel;
	}
	public String getTeacher_password() {
		return Teacher_password;
	}
	public void setTeacher_password(String teacher_password) {
		Teacher_password = teacher_password;
	}
	public String getTeacher_name() {
		return Teacher_name;
	}
	public void setTeacher_name(String teacher_name) {
		Teacher_name = teacher_name;
	}
}
