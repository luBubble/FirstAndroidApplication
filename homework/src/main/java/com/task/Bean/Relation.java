package com.task.Bean;

public class Relation {
	private int Relation_id;
	private int Relation_status;
	private int Relation_state;
	private int Relation_studentId;
	private int Relation_teacherId;
	public int getRelation_id() {
		return Relation_id;
	}
	public void setRelation_id(int relation_id) {
		Relation_id = relation_id;
	}
	public int getRelation_status() {
		return Relation_status;
	}
	public void setRelation_status(int relation_status) {
		Relation_status = relation_status;
	}
	public int getRelation_state() {
		return Relation_state;
	}
	public void setRelation_state(int relation_state) {
		Relation_state = relation_state;
	}
	public int getRelation_studentId() {
		return Relation_studentId;
	}
	public void setRelation_studentId(int relation_studentId) {
		Relation_studentId = relation_studentId;
	}
	public int getRelation_teacherId() {
		return Relation_teacherId;
	}
	public void setRelation_teacherId(int relation_teacherId) {
		Relation_teacherId = relation_teacherId;
	}
}
